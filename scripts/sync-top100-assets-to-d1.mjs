#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import { parseCsv } from "../src/lib/csv.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const args = parseArgs(process.argv.slice(2));
const snapshotDate = args.snapshotDate || new Date().toISOString().slice(0, 10);
const outputPath = path.resolve(args.out || path.join(repoRoot, "data", "imports", "top100-asset-sync.sql"));

const top100Rows = parseCsv(fs.readFileSync(path.join(repoRoot, "data", "mlb-top100-2026.csv"), "utf8"));
const statsRows = parseCsv(fs.readFileSync(path.join(repoRoot, "data", "current-stats.csv"), "utf8"));
const enrichmentRows = parseCsv(fs.readFileSync(path.join(repoRoot, "data", "player-enrichment.csv"), "utf8"));
const statsByPlayerId = new Map(statsRows.map((row) => [String(row.player_id || ""), row]));
const enrichmentByPlayerId = new Map(enrichmentRows.map((row) => [String(row.player_id || ""), row]));

const statements = [
  "-- Generated by scripts/sync-top100-assets-to-d1.mjs.",
  `-- Snapshot date: ${snapshotDate}`,
];
const summary = {
  top100Rows: 0,
  statSnapshotsPrepared: 0,
  outputPath,
};

for (const row of top100Rows) {
  if (!row.player_id || !row.player_name) continue;
  const enrichment = enrichmentByPlayerId.get(String(row.player_id)) || {};
  const player = normalizeTop100Player(row, enrichment);
  statements.push(upsertPlayerStatement(player));
  statements.push(upsertTrackingStatement(player));
  statements.push(playerSourceStatement(player));
  summary.top100Rows += 1;

  const statRow = statsByPlayerId.get(String(row.player_id));
  if (statRow) {
    statements.push(insertStatsStatement(player, statRow));
    summary.statSnapshotsPrepared += 1;
  }
}

statements.push(refreshRunStatement(summary));
writeAndMaybeExecute();

function normalizeTop100Player(row, enrichment) {
  const level = clean(enrichment.current_level || row.level);
  const team = clean(enrichment.current_team || row.stat_team || "");
  return {
    assetId: clean(row.player_id),
    mlbamId: integer(row.mlbam_id),
    playerName: clean(row.player_name),
    fullName: clean(row.player_name),
    normalizedName: normalizeName(row.player_name),
    org: clean(row.org),
    team,
    level,
    position: clean(row.position),
    age: number(row.age),
    bats: clean(row.bats),
    throws: clean(row.throws),
    heightWeight: clean(row.height_weight),
    sourceUrl: clean(row.source_url),
    rank: integer(row.prospect_rank),
    eta: clean(row.eta),
    calledUp: bool(enrichment.called_up),
    on40man: bool(row.on_40man || enrichment.on_40man),
  };
}

function upsertPlayerStatement(player) {
  return `UPDATE players SET
  mlbam_id = ${player.mlbamId},
  identity_key = ${sql(`mlbam:${player.mlbamId}`)},
  player_name = ${sql(player.playerName)},
  full_name = ${sql(player.fullName)},
  current_team = COALESCE(${sql(player.team)}, current_team),
  current_org = COALESCE(${sql(player.org)}, current_org),
  current_level = COALESCE(${sql(player.level)}, current_level),
  position = COALESCE(${sql(player.position)}, position),
  bats = COALESCE(${sql(player.bats)}, bats),
  throws = COALESCE(${sql(player.throws)}, throws),
  age = COALESCE(${number(player.age)}, age),
  active_status = CASE WHEN ${player.calledUp ? 1 : 0} = 1 THEN active_status ELSE 'active' END,
  first_seen_source = COALESCE(first_seen_source, 'MLB Pipeline Top 100 Asset Sync'),
  last_seen_date = ${sql(snapshotDate)},
  updated_at = CURRENT_TIMESTAMP
WHERE mlbam_id = ${player.mlbamId}
  OR lower(player_name) = lower(${sql(player.playerName)})
  OR normalized_name = ${sql(player.normalizedName)};

INSERT OR IGNORE INTO players (
  player_name, normalized_name, mlbam_id, identity_key, full_name,
  current_team, current_org, current_level, position, bats, throws, age,
  active_status, first_seen_source, last_seen_date
)
SELECT
  ${sql(player.playerName)}, ${sql(player.normalizedName)}, ${player.mlbamId}, ${sql(`mlbam:${player.mlbamId}`)},
  ${sql(player.fullName)}, ${sql(player.team)}, ${sql(player.org)}, ${sql(player.level)},
  ${sql(player.position)}, ${sql(player.bats)}, ${sql(player.throws)}, ${number(player.age)},
  'active', 'MLB Pipeline Top 100 Asset Sync', ${sql(snapshotDate)}
WHERE NOT EXISTS (
  SELECT 1 FROM players
  WHERE mlbam_id = ${player.mlbamId}
    OR lower(player_name) = lower(${sql(player.playerName)})
    OR normalized_name = ${sql(player.normalizedName)}
);`;
}

function upsertTrackingStatement(player) {
  const priorityTier = player.calledUp ? "called_up" : "top100_current";
  const trackingGroup = player.calledUp ? "called_up" : "top100";
  const status = player.calledUp ? "graduated" : "active";
  return `INSERT INTO player_tracking_status (
  player_id, tracking_group, priority_tier, status, last_reviewed_at, next_refresh_due, notes
)
SELECT id, ${sql(trackingGroup)}, ${sql(priorityTier)}, ${sql(status)}, ${sql(snapshotDate)}, DATE(${sql(snapshotDate)}, '+14 day'),
  ${sql(`Current Top 100 asset sync: rank ${player.rank || ""}${player.level ? `, ${player.level}` : ""}.`)}
FROM players
WHERE mlbam_id = ${player.mlbamId}
  OR lower(player_name) = lower(${sql(player.playerName)})
  OR normalized_name = ${sql(player.normalizedName)}
ON CONFLICT(player_id) DO UPDATE SET
  tracking_group = excluded.tracking_group,
  priority_tier = excluded.priority_tier,
  status = excluded.status,
  last_reviewed_at = excluded.last_reviewed_at,
  next_refresh_due = excluded.next_refresh_due,
  notes = excluded.notes;`;
}

function playerSourceStatement(player) {
  return `INSERT OR IGNORE INTO player_sources (player_id, source_type, source_name, source_date, source_notes)
SELECT id, 'MLB Pipeline Top 100', 'Current Top 100 asset sync', ${sql(snapshotDate)},
  ${sql(`Asset id ${player.assetId}; rank ${player.rank || ""}; ETA ${player.eta || "n/a"}; source ${player.sourceUrl || "n/a"}.`)}
FROM players
WHERE mlbam_id = ${player.mlbamId}
  OR lower(player_name) = lower(${sql(player.playerName)})
  OR normalized_name = ${sql(player.normalizedName)};`;
}

function insertStatsStatement(player, row) {
  const isPitcher = hasValue(row.era) || hasValue(row.whip) || String(player.position).toUpperCase().includes("P");
  const role = isPitcher ? "Pitcher" : "Hitter";
  return `INSERT INTO player_stats_snapshots (
  player_id, season, stats_role, level, team, age,
  hitter_games, hitter_pa, hitter_avg, hitter_obp, hitter_slg, hitter_ops,
  hitter_hr, hitter_sb, hitter_bb_pct, hitter_k_pct,
  pitcher_games, pitcher_era, pitcher_whip, pitcher_k_pct, pitcher_bb_pct,
  stats_match_status, stats_source, snapshot_date, raw_payload_json
)
SELECT id, 2026, ${sql(role)}, ${sql(row.stat_level || player.level)}, ${sql(row.stat_team || player.team)}, ${number(player.age)},
  ${integer(row.games)}, ${integer(row.pa)}, ${number(row.avg)}, ${number(row.obp)}, ${number(row.slg)}, ${number(row.ops)},
  ${integer(row.hr)}, ${integer(row.sb)}, ${pctToDecimal(row.bb_rate)}, ${pctToDecimal(row.k_rate)},
  ${integer(row.games)}, ${number(row.era)}, ${number(row.whip)}, ${perNineToProxy(row.k_per_9)}, ${perNineToProxy(row.bb_per_9)},
  'Matched via MLB Top 100 asset sync', 'MLB Pipeline Top 100 Stats Asset', ${sql(snapshotDate)},
  ${sql(JSON.stringify({ source: "data/current-stats.csv", player_id: row.player_id }))}
FROM players
WHERE mlbam_id = ${player.mlbamId}
  OR lower(player_name) = lower(${sql(player.playerName)})
  OR normalized_name = ${sql(player.normalizedName)}
ON CONFLICT(player_id, season, stats_role, level, team, snapshot_date) DO UPDATE SET
  age = excluded.age,
  hitter_games = excluded.hitter_games,
  hitter_pa = excluded.hitter_pa,
  hitter_avg = excluded.hitter_avg,
  hitter_obp = excluded.hitter_obp,
  hitter_slg = excluded.hitter_slg,
  hitter_ops = excluded.hitter_ops,
  hitter_hr = excluded.hitter_hr,
  hitter_sb = excluded.hitter_sb,
  hitter_bb_pct = excluded.hitter_bb_pct,
  hitter_k_pct = excluded.hitter_k_pct,
  pitcher_games = excluded.pitcher_games,
  pitcher_era = excluded.pitcher_era,
  pitcher_whip = excluded.pitcher_whip,
  pitcher_k_pct = excluded.pitcher_k_pct,
  pitcher_bb_pct = excluded.pitcher_bb_pct,
  stats_match_status = excluded.stats_match_status,
  stats_source = excluded.stats_source,
  raw_payload_json = excluded.raw_payload_json;`;
}

function refreshRunStatement(result) {
  return `INSERT INTO player_refresh_runs (
  run_type, started_at, finished_at, status, players_seen, stats_snapshots_added, errors_json
) VALUES (
  'sync_top100_assets_to_d1', ${sql(new Date().toISOString())}, ${sql(new Date().toISOString())}, 'success',
  ${integer(result.top100Rows)}, ${integer(result.statSnapshotsPrepared)}, '[]'
);`;
}

function writeAndMaybeExecute() {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, statements.join("\n") + "\n");
  console.log(JSON.stringify(summary, null, 2));
  if (!args.execute) return;
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--file", outputPath];
  if (args.remote) wranglerArgs.push("--remote");
  execFileSync("npx", wranglerArgs, { cwd: repoRoot, stdio: "inherit" });
}

function parseArgs(argv) {
  const result = {
    execute: false,
    remote: false,
    database: "ondeck-market",
    out: "",
    snapshotDate: "",
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--execute") result.execute = true;
    else if (arg === "--remote") result.remote = true;
    else if (arg === "--database") result.database = argv[++index] || result.database;
    else if (arg === "--out") result.out = argv[++index] || "";
    else if (arg === "--snapshot-date") result.snapshotDate = argv[++index] || "";
  }
  return result;
}

function normalizeName(value) {
  return clean(value).normalize("NFD").replaceAll(/[\u0300-\u036f]/g, "").toLowerCase().replaceAll(/[^a-z0-9]+/g, " ").trim();
}

function bool(value) {
  return String(value || "").toLowerCase() === "true" || value === 1;
}

function hasValue(value) {
  return value !== "" && value !== null && value !== undefined;
}

function clean(value) {
  return String(value ?? "").replaceAll(/\s+/g, " ").trim();
}

function sql(value) {
  const text = clean(value);
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function integer(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(Math.round(numeric)) : "NULL";
}

function number(value) {
  const numeric = Number(String(value ?? "").replaceAll(/[^0-9.-]/g, ""));
  return Number.isFinite(numeric) ? String(numeric) : "NULL";
}

function pctToDecimal(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(numeric / 100) : "NULL";
}

function perNineToProxy(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(numeric / 38) : "NULL";
}
