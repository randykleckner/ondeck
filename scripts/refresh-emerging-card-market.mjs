#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const SOLD_COMPS_API_URL = "https://api.sold-comps.com/v1/scrape";
const CACHE_DAYS = 7;
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const args = parseArgs(process.argv.slice(2));
const outputPath = path.resolve(args.out || path.join(repoRoot, "data", "imports", "emerging-market-refresh.sql"));
const now = new Date();
const snapshotDate = args.snapshotDate || now.toISOString().slice(0, 10);
const apiKey = process.env.SOLD_COMPS_API_KEY;

const candidates = readCandidates().filter((row) => !args.player || normalizeName(row.player_name) === normalizeName(args.player));
const selected = candidates.slice(0, args.limit);
const statements = [
  "-- Generated by scripts/refresh-emerging-card-market.mjs.",
  `-- Snapshot date: ${snapshotDate}`,
];
const summary = {
  candidates: candidates.length,
  selected: selected.length,
  skippedFresh: 0,
  apiCallsUsed: 0,
  snapshotsPrepared: 0,
  dryRun: args.dryRun,
  errors: [],
};

if (!args.dryRun && !apiKey) {
  console.error("SOLD_COMPS_API_KEY is required unless --dry-run is used.");
  process.exit(1);
}

for (const row of selected) {
  if (!args.force && isFresh(row.last_snapshot_date)) {
    summary.skippedFresh += 1;
    continue;
  }

  const keyword = cardQuery(row);
  if (args.dryRun) {
    console.log(`[dry-run] ${row.player_name}: ${keyword}`);
    continue;
  }

  try {
    const raw = await fetchSoldComps(apiKey, keyword);
    summary.apiCallsUsed += 1;
    const sold = extractSoldRecords(raw, row.player_name);
    const active = extractActiveListings(raw, row.player_name);
    const snapshot = buildSnapshot({ row, keyword, sold, active, raw });
    statements.push(insertSnapshotStatement(snapshot));
    summary.snapshotsPrepared += 1;
  } catch (error) {
    summary.errors.push({ player: row.player_name, message: error instanceof Error ? error.message : "Unknown error" });
  }
}

statements.push(pipelineRunStatement(summary));

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, statements.join("\n") + "\n");

console.log(JSON.stringify({ ...summary, outputPath }, null, 2));

if (!args.dryRun && summary.snapshotsPrepared > 0) {
  executeSql(outputPath);
}

function readCandidates() {
  const rows = queryD1(`
    SELECT
      p.id AS player_id,
      p.player_name,
      ct.id AS card_target_id,
      ct.year,
      ct.product,
      ct.auto_code,
      ct.card_query_seed,
      (
        SELECT MAX(snapshot_date)
        FROM card_market_snapshots m
        WHERE m.player_id = p.id AND m.card_target_id = ct.id
      ) AS last_snapshot_date
    FROM player_tracking_status pts
    JOIN players p ON p.id = pts.player_id
    JOIN emerging_card_targets ct ON ct.player_id = p.id AND ct.active = 1
    WHERE pts.tracking_group = 'emerging'
      AND pts.status = 'active'
      AND pts.priority_tier = 'card_api_candidate'
    ORDER BY p.player_name ASC, ct.year DESC
    LIMIT ${integer(args.limit)}
  `);
  return rows;
}

function queryD1(sql) {
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--command", sql, "--json"];
  if (args.remote) wranglerArgs.push("--remote");
  const stdout = execFileSync("npx", wranglerArgs, { cwd: repoRoot, encoding: "utf8" });
  const parsed = JSON.parse(stdout);
  if (Array.isArray(parsed)) {
    return parsed.flatMap((entry) => entry.results || entry.result?.[0]?.results || []);
  }
  return parsed.results || parsed.result?.[0]?.results || [];
}

function executeSql(filePath) {
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--file", filePath];
  if (args.remote) wranglerArgs.push("--remote");
  execFileSync("npx", wranglerArgs, { cwd: repoRoot, stdio: "inherit" });
}

async function fetchSoldComps(apiKey, keyword) {
  const url = new URL(SOLD_COMPS_API_URL);
  url.searchParams.set("keyword", keyword);
  const response = await fetch(url, {
    headers: {
      Authorization: `Bearer ${apiKey}`,
      Accept: "application/json",
    },
  });
  const text = await response.text();
  const raw = text ? JSON.parse(text) : {};
  if (!response.ok) {
    throw new Error(raw?.message || raw?.error || `SoldComps failed with ${response.status}`);
  }
  return raw;
}

function buildSnapshot({ row, keyword, sold, active, raw }) {
  const sold7 = inWindow(sold, 7);
  const sold30 = inWindow(sold, 30);
  const sold90 = inWindow(sold, 90);
  const activePrices = active.map((item) => item.price).filter(Number.isFinite).sort((a, b) => a - b);
  const activeCount = active.length || activeListingCountFromRaw(raw);
  return {
    playerId: row.player_id,
    cardTargetId: row.card_target_id,
    source: "SoldComps API",
    salesCount7d: sold7.length,
    salesCount30d: sold30.length,
    salesCount90d: sold90.length,
    avgPrice7d: average(sold7.map((item) => item.price)),
    avgPrice30d: average(sold30.map((item) => item.price)),
    avgPrice90d: average(sold90.map((item) => item.price)),
    medianPrice30d: median(sold30.map((item) => item.price)),
    medianPrice90d: median(sold90.map((item) => item.price)),
    lowSold30d: minValue(sold30.map((item) => item.price)),
    highSold30d: maxValue(sold30.map((item) => item.price)),
    lastSoldPrice: sold[0]?.price ?? null,
    lastSoldAt: sold[0]?.soldAt ?? null,
    activeListingCount: Number.isFinite(activeCount) ? activeCount : null,
    activeLowestAsk: activePrices[0] ?? null,
    activeMedianAsk: median(activePrices),
    sellThrough30d: Number.isFinite(activeCount) && activeCount > 0 ? sold30.length / activeCount : null,
    sellThrough90d: Number.isFinite(activeCount) && activeCount > 0 ? sold90.length / activeCount : null,
    marketSignal: marketSignal(sold30, sold90),
    snapshotDate,
    rawPayloadJson: JSON.stringify({ keyword, rawKeys: Object.keys(raw || {}) }),
  };
}

function insertSnapshotStatement(row) {
  return `INSERT INTO card_market_snapshots (
  player_id, card_target_id, source, sales_count_7d, sales_count_30d, sales_count_90d,
  avg_price_7d, avg_price_30d, avg_price_90d, median_price_30d, median_price_90d,
  low_sold_30d, high_sold_30d, last_sold_price, last_sold_at,
  active_listing_count, active_lowest_ask, active_median_ask,
  sell_through_30d, sell_through_90d, market_signal, snapshot_date, raw_payload_json
) VALUES (
  ${integer(row.playerId)}, ${integer(row.cardTargetId)}, ${sql(row.source)}, ${integer(row.salesCount7d)}, ${integer(row.salesCount30d)}, ${integer(row.salesCount90d)},
  ${number(row.avgPrice7d)}, ${number(row.avgPrice30d)}, ${number(row.avgPrice90d)}, ${number(row.medianPrice30d)}, ${number(row.medianPrice90d)},
  ${number(row.lowSold30d)}, ${number(row.highSold30d)}, ${number(row.lastSoldPrice)}, ${sql(row.lastSoldAt)},
  ${integer(row.activeListingCount)}, ${number(row.activeLowestAsk)}, ${number(row.activeMedianAsk)},
  ${number(row.sellThrough30d)}, ${number(row.sellThrough90d)}, ${sql(row.marketSignal)}, ${sql(row.snapshotDate)}, ${sql(row.rawPayloadJson)}
)
ON CONFLICT(player_id, card_target_id, source, snapshot_date) DO UPDATE SET
  sales_count_7d = excluded.sales_count_7d,
  sales_count_30d = excluded.sales_count_30d,
  sales_count_90d = excluded.sales_count_90d,
  avg_price_7d = excluded.avg_price_7d,
  avg_price_30d = excluded.avg_price_30d,
  avg_price_90d = excluded.avg_price_90d,
  median_price_30d = excluded.median_price_30d,
  median_price_90d = excluded.median_price_90d,
  low_sold_30d = excluded.low_sold_30d,
  high_sold_30d = excluded.high_sold_30d,
  last_sold_price = excluded.last_sold_price,
  last_sold_at = excluded.last_sold_at,
  active_listing_count = excluded.active_listing_count,
  active_lowest_ask = excluded.active_lowest_ask,
  active_median_ask = excluded.active_median_ask,
  sell_through_30d = excluded.sell_through_30d,
  sell_through_90d = excluded.sell_through_90d,
  market_signal = excluded.market_signal,
  raw_payload_json = excluded.raw_payload_json;`;
}

function pipelineRunStatement(summary) {
  return `INSERT INTO pipeline_runs (
  run_type, started_at, finished_at, status, players_checked, card_targets_checked,
  market_snapshots_added, errors_json
) VALUES (
  'refresh_emerging_card_market', ${sql(now.toISOString())}, ${sql(new Date().toISOString())}, ${sql(summary.errors.length ? "partial" : "success")},
  ${integer(summary.selected)}, ${integer(summary.selected)}, ${integer(summary.snapshotsPrepared)}, ${sql(JSON.stringify(summary.errors))}
);`;
}

function extractSoldRecords(raw, playerName) {
  return extractArray(raw, ["results", "data.results", "data.items", "data", "comps", "sales", "items"])
    .map((item) => ({
      title: clean(item.title || item.name || item.itemTitle),
      price: money(item.price ?? item.sale_price ?? item.salePrice ?? item.sold_price ?? item.soldPrice ?? item.amount ?? item.value),
      soldAt: normalizeDate(item.sold_at || item.soldAt || item.sale_date || item.saleDate || item.date || item.ended_at || item.endedAt),
    }))
    .filter((item) => Number.isFinite(item.price) && item.soldAt && benchmarkTitle(item.title, playerName))
    .sort((a, b) => new Date(`${b.soldAt}T00:00:00Z`) - new Date(`${a.soldAt}T00:00:00Z`));
}

function extractActiveListings(raw, playerName) {
  const active = raw.active_listings || raw.activeListings || raw.active?.items || raw.active?.results || raw.listings || raw.items_active || raw.data?.activeListings;
  return extractAnyArray(active)
    .map((item) => ({
      title: clean(item.title || item.name || item.itemTitle),
      price: money(item.price ?? item.current_price ?? item.currentPrice ?? item.askingPrice ?? item.amount ?? item.value),
    }))
    .filter((item) => Number.isFinite(item.price) && benchmarkTitle(item.title, playerName));
}

function benchmarkTitle(title, playerName) {
  const text = normalizeName(title);
  const playerTokens = normalizeName(playerName).split(" ").filter(Boolean);
  return playerTokens.every((token) => text.includes(token))
    && !/(psa|bgs|sgc|cgc|graded|refractor|sapphire|purple|blue|gold|orange|green|mojo|wave|speckle|paper|numbered|\/99|\/75|\/50|\/25|\/10|\/5|insert|lot|break|digital|reprint|custom)/i.test(title);
}

function marketSignal(sold30, sold90) {
  if (!sold90.length) return "No Liquidity";
  if (sold90.length <= 3) return "Thin";
  const avg30 = average(sold30.map((item) => item.price));
  const avg90 = average(sold90.map((item) => item.price));
  if (sold30.length >= sold90.length * 0.5 && Number.isFinite(avg30) && Number.isFinite(avg90) && avg30 > avg90) return "Heating";
  if (sold30.length >= 8 && Number.isFinite(avg30) && Number.isFinite(avg90) && avg30 > avg90 * 1.35) return "Priced In";
  if (sold90.length >= 4) return "Liquid";
  return "Pending";
}

function cardQuery(row) {
  return clean(row.card_query_seed) || `${row.year} ${row.product} ${row.player_name} ${row.auto_code} auto 1st -break -lot -reprint`;
}

function isFresh(value) {
  if (!value) return false;
  const date = new Date(`${value}T00:00:00Z`);
  if (Number.isNaN(date.getTime())) return false;
  return (now - date) / 86400000 < CACHE_DAYS;
}

function inWindow(records, days) {
  const cutoff = new Date(now);
  cutoff.setUTCDate(cutoff.getUTCDate() - days);
  return records.filter((record) => new Date(`${record.soldAt}T00:00:00Z`) >= cutoff);
}

function extractArray(raw, paths) {
  for (const pathSpec of paths) {
    const value = pathSpec.split(".").reduce((current, key) => current?.[key], raw);
    const records = extractAnyArray(value);
    if (records.length) return records;
  }
  return [];
}

function extractAnyArray(value) {
  if (Array.isArray(value)) return value;
  if (Array.isArray(value?.results)) return value.results;
  if (Array.isArray(value?.items)) return value.items;
  return [];
}

function activeListingCountFromRaw(raw) {
  const count = Number(raw.active_listing_count ?? raw.activeListingCount ?? raw.data?.active_listing_count ?? raw.data?.activeListingCount);
  return Number.isFinite(count) ? count : null;
}

function parseArgs(argv) {
  const result = {
    limit: 150,
    dryRun: false,
    player: "",
    force: false,
    remote: false,
    database: "ondeck-market",
    out: "",
    snapshotDate: "",
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--limit") result.limit = Math.max(1, Math.min(150, Number(argv[++index]) || 150));
    else if (arg === "--dry-run") result.dryRun = true;
    else if (arg === "--player") result.player = argv[++index] || "";
    else if (arg === "--force") result.force = true;
    else if (arg === "--remote") result.remote = true;
    else if (arg === "--database") result.database = argv[++index] || result.database;
    else if (arg === "--out") result.out = argv[++index] || "";
    else if (arg === "--snapshot-date") result.snapshotDate = argv[++index] || "";
  }
  return result;
}

function normalizeDate(value) {
  const date = new Date(value);
  return Number.isNaN(date.getTime()) ? "" : date.toISOString().slice(0, 10);
}

function average(values) {
  const numbers = values.filter(Number.isFinite);
  return numbers.length ? numbers.reduce((sum, value) => sum + value, 0) / numbers.length : null;
}

function median(values) {
  const numbers = values.filter(Number.isFinite).sort((a, b) => a - b);
  if (!numbers.length) return null;
  const middle = Math.floor(numbers.length / 2);
  return numbers.length % 2 ? numbers[middle] : (numbers[middle - 1] + numbers[middle]) / 2;
}

function minValue(values) {
  const numbers = values.filter(Number.isFinite);
  return numbers.length ? Math.min(...numbers) : null;
}

function maxValue(values) {
  const numbers = values.filter(Number.isFinite);
  return numbers.length ? Math.max(...numbers) : null;
}

function normalizeName(value) {
  return clean(value).normalize("NFD").replaceAll(/[\u0300-\u036f]/g, "").toLowerCase().replaceAll(/[^a-z0-9]+/g, " ").trim();
}

function clean(value) {
  return String(value ?? "").replaceAll(/\s+/g, " ").trim();
}

function sql(value) {
  const text = clean(value);
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function integer(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(Math.round(numeric)) : "NULL";
}

function number(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(numeric) : "NULL";
}

function money(value) {
  const numeric = Number(String(value ?? "").replaceAll(/[^0-9.-]/g, ""));
  return Number.isFinite(numeric) ? numeric : NaN;
}
