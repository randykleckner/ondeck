function queryD1(command) {
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--command", command, "--json"];
  if (args.remote) wranglerArgs.push("--remote");
  const stdout = execFileSync("npx", wranglerArgs, { cwd: repoRoot, encoding: "utf8", maxBuffer: 20 * 1024 * 1024 });
  const parsed = JSON.parse(stdout);
  if (Array.isArray(parsed)) return parsed.flatMap((entry) => entry.results || entry.result?.[0]?.results || []);
  return parsed.results || parsed.result?.[0]?.results || [];
}

function executeSql(filePath) {
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--file", filePath];
  if (args.remote) wranglerArgs.push("--remote");
  execFileSync("npx", wranglerArgs, { cwd: repoRoot, stdio: "inherit" });
}

async function fetchText(url) {
  const response = await fetch(url, {
    headers: {
      Accept: "text/html,application/xhtml+xml",
      "User-Agent": "OnDeckProspect/1.0 checklist-ingestion",
    },
  });
  if (!response.ok) throw new Error(`${url} returned ${response.status}`);
  return response.text();
}

function nearestSubsetHeading(html, index) {
  const before = html.slice(Math.max(0, index - 8000), index);
  const headingMatches = [...before.matchAll(/<h[1-6][^>]*>([\s\S]*?)<\/h[1-6]>|<(?:strong|b)[^>]*>([\s\S]*?)<\/(?:strong|b)>/gi)];
  for (const match of headingMatches.reverse()) {
    const heading = targetSectionFromText(clean(decodeHtml(stripTags(match[1] || match[2] || ""))));
    if (heading) return heading;
  }
  return "";
}

function targetSectionFromText(text) {
  const normalized = normalizeLoose(text);
  return TARGET_SECTIONS.find((section) => normalized.includes(normalizeLoose(section))) || "";
}

function isTargetSection(value) {
  return Boolean(targetSectionFromText(value));
}

function cardCodeFromText(text) {
  const candidates = [...String(text || "").matchAll(/\b([A-Z]{2,6}-?[A-Z0-9]{1,8})\b/g)]
    .map((match) => normalizeCardCode(match[1]))
    .filter(isLikelyAutoCode);
  return candidates[0] || "";
}

function isLikelyAutoCode(code) {
  const normalized = normalizeCardCode(code);
  return /^(CPA|CDA|BDA|BCPA|BCPA|PA|DA|BDC|BCA|CPAA|CDAA|CDPA)-?[A-Z0-9]+$/.test(normalized)
    || (/^[A-Z]{3,6}[A-Z0-9]{1,5}$/.test(normalized) && normalized.includes("A"));
}

function normalizeCardCode(value) {
  return clean(value).toUpperCase().replace(/[^A-Z0-9-]/g, "");
}

function generatedCpaCode(name) {
  const parts = cleanName(name).split(/\s+/).filter(Boolean);
  if (!parts.length) return "";
  const first = parts[0][0] || "";
  const last = parts.length > 1 ? parts.at(-1)[0] : "";
  return `CPA-${first}${last}`.toUpperCase();
}

function canonicalQuery(playerName, cardCode) {
  return `${playerName} ${cardCode} Bowman Chrome 1st Auto`;
}

function fuzzyCandidates(normalizedPlayerName, players) {
  if (!normalizedPlayerName) return [];
  return players
    .map((player) => ({ player, distance: levenshtein(normalizedPlayerName, normalizeName(player.player_name)) }))
    .filter((entry) => entry.distance <= 2)
    .sort((a, b) => a.distance - b.distance)
    .slice(0, 3)
    .map((entry) => entry.player);
}

function teamMatches(cardTeam, player) {
  const team = normalizeName(cardTeam);
  if (!team) return false;
  const org = normalizeName(player.current_org);
  const currentTeam = normalizeName(player.current_team);
  return Boolean((org && (org.includes(team) || team.includes(org))) || (currentTeam && (currentTeam.includes(team) || team.includes(currentTeam))));
}

function isTrackedPlayer(player) {
  return String(player.tracking_status || "").toLowerCase() === "active"
    || ["emerging", "watchlist", "top100"].includes(String(player.tracking_group || "").toLowerCase());
}

function confidenceLabel(match) {
  if (match.confidence === "high") return "checklist_high_name_team";
  if (match.confidence === "medium") return match.sameNameCollision ? "checklist_medium_collision_team" : "checklist_medium_name";
  return match.sameNameCollision ? "checklist_low_same_name_collision" : "checklist_low_fuzzy";
}

function checklistKey(card) {
  return [
    card.productYear,
    card.productFamily,
    card.productName,
    card.subsetName,
    card.cardCode,
    card.normalizedPlayerName,
  ].join("|");
}

function cleanName(value) {
  return clean(value)
    .replace(/\([^)]*\)/g, " ")
    .replace(/\b(auto|autograph|prospect|chrome|variation|refractor|image|short print|sp)\b/gi, " ")
    .replace(/^[#\-\s]+/, "")
    .replace(/\s+/g, " ")
    .trim();
}

function cleanTeam(value) {
  return clean(value).replace(/\([^)]*\)/g, "").replace(/\b(auto|autograph)\b/gi, "").trim();
}

function looksLikePlayerName(value) {
  const text = clean(value);
  return /^[A-Za-zÀ-ÿ' .-]{3,}$/.test(text) && /\s/.test(text);
}

function htmlToText(html) {
  return decodeHtml(String(html || "")
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<br\s*\/?>/gi, "\n")
    .replace(/<\/(?:tr|p|li|div|h[1-6])>/gi, "\n")
    .replace(/<[^>]+>/g, " "));
}

function stripTags(value) {
  return String(value || "").replace(/<[^>]+>/g, " ");
}

function decodeHtml(value) {
  return String(value || "")
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, "\"")
    .replace(/&#039;|&apos;/g, "'")
    .replace(/&nbsp;/g, " ")
    .replace(/&ndash;|&#8211;/g, "-")
    .replace(/&mdash;|&#8212;/g, "-")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">");
}

function decodeDuckDuckGoUrl(value) {
  try {
    const url = new URL(value, "https://duckduckgo.com");
    const uddg = url.searchParams.get("uddg");
    return uddg ? decodeURIComponent(uddg) : url.toString();
  } catch {
    return value;
  }
}

function dedupeSources(sources) {
  const seen = new Set();
  const unique = [];
  for (const source of sources) {
    const key = source.url.replace(/#.*$/, "");
    if (seen.has(key)) continue;
    seen.add(key);
    unique.push({ ...source, url: key });
  }
  return unique;
}

function parseArgs(argv) {
  const currentYear = new Date().getFullYear();
  const result = {
    years: range(2023, currentYear),
    productFamilies: [],
    maxPagesPerProduct: 4,
    search: true,
    execute: false,
    remote: false,
    database: "ondeck-market",
    out: "",
    generateFallbacks: false,
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--years") result.years = String(argv[++index] || "").split(",").map((year) => Number(year.trim())).filter(Number.isFinite);
    else if (arg === "--year") result.years = [Number(argv[++index])].filter(Number.isFinite);
    else if (arg === "--products") result.productFamilies = String(argv[++index] || "").split(",").map(clean).filter(Boolean);
    else if (arg === "--max-pages-per-product") result.maxPagesPerProduct = Math.max(1, Math.min(10, Number(argv[++index]) || 4));
    else if (arg === "--no-search") result.search = false;
    else if (arg === "--execute") result.execute = true;
    else if (arg === "--remote") result.remote = true;
    else if (arg === "--database") result.database = argv[++index] || result.database;
    else if (arg === "--out") result.out = argv[++index] || "";
    else if (arg === "--generate-fallbacks") result.generateFallbacks = true;
  }
  return result;
}

function range(start, end) {
  return Array.from({ length: Math.max(0, end - start + 1) }, (_, index) => start + index);
}

function normalizeName(value) {
  return clean(value).normalize("NFD").replace(/[\u0300-\u036f]/g, "").toLowerCase().replace(/[^a-z0-9]+/g, " ").trim();
}

function normalizeLoose(value) {
  return normalizeName(value).replace(/\s+/g, " ");
}

function clean(value) {
  return String(value ?? "").replace(/\s+/g, " ").trim();
}

function sql(value) {
  const text = clean(value);
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function nullSafeIs(value) {
  const text = clean(value);
  return text ? sql(text) : "NULL";
}

function integer(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(Math.round(numeric)) : "NULL";
}

function errorMessage(error) {
  return error instanceof Error ? error.message : String(error);
}

function levenshtein(a, b) {
  const matrix = Array.from({ length: a.length + 1 }, () => Array(b.length + 1).fill(0));
  for (let i = 0; i <= a.length; i += 1) matrix[i][0] = i;
  for (let j = 0; j <= b.length; j += 1) matrix[0][j] = j;
  for (let i = 1; i <= a.length; i += 1) {
    for (let j = 1; j <= b.length; j += 1) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1;
      matrix[i][j] = Math.min(
        matrix[i - 1][j] + 1,
        matrix[i][j - 1] + 1,
        matrix[i - 1][j - 1] + cost,
      );
    }
  }
  return matrix[a.length][b.length];
}
