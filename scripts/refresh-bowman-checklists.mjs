function checklistCardIdSubquery(card) {
  return `(SELECT checklist_card_id FROM bowman_checklist_cards
    WHERE product_year = ${integer(card.productYear)}
      AND product_family = ${sql(card.productFamily)}
      AND product_name = ${sql(card.productName)}
      AND subset_name IS ${nullSafeIs(card.subsetName)}
      AND card_code = ${sql(card.cardCode)}
      AND normalized_player_name = ${sql(card.normalizedPlayerName)}
    LIMIT 1)`;
}

