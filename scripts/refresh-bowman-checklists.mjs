#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const args = parseArgs(process.argv.slice(2));
const now = new Date();
const importedAt = now.toISOString();
const outputPath = path.resolve(args.out || path.join(repoRoot, "data", "imports", "bowman-checklist-refresh.sql"));

const PRODUCTS = [
  { family: "Bowman Baseball", slug: "bowman-baseball", productName: "Bowman Baseball" },
  { family: "Bowman Chrome Baseball", slug: "bowman-chrome-baseball", productName: "Bowman Chrome Baseball" },
  { family: "Bowman Draft Baseball", slug: "bowman-draft-baseball", productName: "Bowman Draft Baseball" },
];

const TARGET_SECTIONS = [
  "Bowman Chrome Prospect Autographs",
  "Chrome Prospect Autographs",
  "Prospect Autographs",
  "Bowman Draft Chrome Prospect Autographs",
  "Chrome Prospect Autograph Variations",
  "Prospect Autograph Variations",
  "Mega Box Prospect Autographs",
];

const summary = {
  years: args.years,
  products: args.productFamilies.length ? args.productFamilies : PRODUCTS.map((product) => product.family),
  pagesFetched: 0,
  pagesFailed: 0,
  cardsParsed: 0,
  matchedTargets: 0,
  highConfidence: 0,
  needsReview: 0,
  fallbackGuesses: 0,
  errors: [],
  dryRun: !args.execute,
};

const players = readPlayers();
const playerIndex = buildPlayerIndex(players);
const checklistCards = [];
const seenCards = new Set();

for (const year of args.years) {
  for (const product of PRODUCTS.filter((item) => !args.productFamilies.length || args.productFamilies.includes(item.family))) {
    const urls = await discoverUrls(year, product);
    for (const source of urls.slice(0, args.maxPagesPerProduct)) {
      try {
        const html = await fetchText(source.url);
        summary.pagesFetched += 1;
        const cards = parseChecklistPage(html, { year, product, source });
        for (const card of cards) {
          const key = checklistKey(card);
          if (seenCards.has(key)) continue;
          seenCards.add(key);
          checklistCards.push(card);
        }
      } catch (error) {
        summary.pagesFailed += 1;
        summary.errors.push({ year, product: product.family, source: source.sourceName, url: source.url, message: errorMessage(error) });
      }
    }
  }
}

summary.cardsParsed = checklistCards.length;

const statements = [
  "-- Generated by scripts/refresh-bowman-checklists.mjs.",
  `-- Imported at: ${importedAt}`,
];

for (const card of checklistCards) {
  statements.push(upsertChecklistCardStatement(card));
  const match = matchCardToPlayer(card, playerIndex);
  if (!match) continue;
  summary.matchedTargets += 1;
  if (match.confidence === "high") summary.highConfidence += 1;
  else summary.needsReview += 1;
  statements.push(upsertEmergingCardTargetStatement(card, match));
  statements.push(upsertTop100CardTargetStatement(card, match));
}

if (args.generateFallbacks) {
  const matchedPlayerIds = new Set(checklistCards.map((card) => matchCardToPlayer(card, playerIndex)?.player?.id).filter(Boolean));
  for (const player of players.filter((row) => isTrackedPlayer(row) && !matchedPlayerIds.has(row.id))) {
    const code = generatedCpaCode(player.player_name);
    if (!code) continue;
    summary.fallbackGuesses += 1;
    statements.push(upsertGeneratedFallbackTargetStatement(player, code));
  }
}

statements.push(refreshRunStatement());

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, statements.join("\n") + "\n");

console.log(JSON.stringify({ ...summary, outputPath }, null, 2));

if (args.execute) {
  executeSql(outputPath);
}

async function discoverUrls(year, product) {
  const known = knownSourceUrls(year, product);
  if (!args.search) return known;
  const searched = await searchChecklistUrls(year, product).catch((error) => {
    summary.errors.push({ year, product: product.family, source: "Search", message: errorMessage(error) });
    return [];
  });
  return dedupeSources([...known, ...searched]);
}

function knownSourceUrls(year, product) {
  const baseSlug = `${year}-${product.slug}`;
  return [
    {
      sourceName: "Beckett",
      url: `https://www.beckett.com/news/${baseSlug}-cards/`,
    },
    {
      sourceName: "Cardboard Connection",
      url: `https://www.cardboardconnection.com/${baseSlug}-cards`,
    },
    {
      sourceName: "Topps Ripped",
      url: `https://ripped.topps.com/${baseSlug}-checklist/`,
    },
    {
      sourceName: "Topps Ripped",
      url: `https://ripped.topps.com/${baseSlug}/`,
    },
    {
      sourceName: "SportsCardsPro",
      url: `https://www.sportscardspro.com/console/baseball-cards-${year}-${product.slug}`,
    },
  ];
}

async function searchChecklistUrls(year, product) {
  const query = `${year} ${product.family} checklist`;
  const url = new URL("https://duckduckgo.com/html/");
  url.searchParams.set("q", query);
  const html = await fetchText(url.toString());
  const urls = [...html.matchAll(/<a[^>]+class="result__a"[^>]+href="([^"]+)"/gi)]
    .map((match) => decodeDuckDuckGoUrl(decodeHtml(match[1])))
    .filter((href) => /beckett\.com|cardboardconnection\.com|ripped\.topps\.com|topps\.com|sportscardspro\.com/i.test(href))
    .map((href) => sourceFromUrl(href));
  return urls;
}

function sourceFromUrl(url) {
  if (/beckett\.com/i.test(url)) return { sourceName: "Beckett", url };
  if (/cardboardconnection\.com/i.test(url)) return { sourceName: "Cardboard Connection", url };
  if (/ripped\.topps\.com|topps\.com/i.test(url)) return { sourceName: "Topps / Ripped", url };
  if (/sportscardspro\.com/i.test(url)) return { sourceName: "SportsCardsPro", url };
  return { sourceName: "Checklist Search", url };
}

function parseChecklistPage(html, { year, product, source }) {
  const cards = [];
  cards.push(...parseTableRows(html, { year, product, source }));
  cards.push(...parseTextLines(html, { year, product, source }));
  return cards;
}

function parseTableRows(html, context) {
  const cards = [];
  const rows = [...html.matchAll(/<tr\b[^>]*>([\s\S]*?)<\/tr>/gi)];
  for (const row of rows) {
    const rowHtml = row[0];
    const cells = [...rowHtml.matchAll(/<t[dh]\b[^>]*>([\s\S]*?)<\/t[dh]>/gi)]
      .map((cell) => clean(decodeHtml(stripTags(cell[1]))))
      .filter(Boolean);
    if (cells.length < 2) continue;
    const subsetName = nearestSubsetHeading(html, row.index || 0);
    if (!isTargetSection(subsetName)) continue;
    const parsed = parseChecklistCells(cells);
    if (!parsed) continue;
    cards.push(cardFromParsed(parsed, subsetName, context));
  }
  return cards;
}

function parseTextLines(html, context) {
  const text = htmlToText(html);
  const cards = [];
  let subsetName = "";
  for (const rawLine of text.split("\n")) {
    const line = clean(rawLine);
    if (!line) continue;
    const heading = targetSectionFromText(line);
    if (heading) {
      subsetName = heading;
      continue;
    }
    if (!isTargetSection(subsetName)) continue;
    const parsedLines = parseChecklistLine(line);
    for (const parsed of parsedLines) {
      cards.push(cardFromParsed(parsed, subsetName, context));
    }
  }
  return cards;
}

function parseChecklistCells(cells) {
  const codeIndex = cells.findIndex((cell) => cardCodeFromText(cell));
  if (codeIndex < 0) return null;
  const cardCode = cardCodeFromText(cells[codeIndex]);
  const after = cells.slice(codeIndex + 1).filter(Boolean);
  const before = cells.slice(0, codeIndex).filter(Boolean);
  const playerName = cleanName(after[0] || before.at(-1) || "");
  const team = clean(after[1] || "");
  if (!playerName || !looksLikePlayerName(playerName)) return null;
  return { cardCode, playerName, team };
}

function parseChecklistLine(line) {
  const codeMatches = [...String(line || "").matchAll(/\b([A-Z]{2,6}-?[A-Z0-9]{1,8})\b/g)]
    .map((match) => ({ code: normalizeCardCode(match[1]), index: match.index || 0, raw: match[0] }))
    .filter((match) => isLikelyAutoCode(match.code));
  if (codeMatches.length > 1) {
    return codeMatches
      .map((match, index) => {
        const next = codeMatches[index + 1];
        const segment = clean(line.slice(match.index + match.raw.length, next ? next.index : undefined));
        const parsed = parseChecklistRemainder(match.code, segment);
        return parsed;
      })
      .filter(Boolean);
  }
  const cardCode = codeMatches[0]?.code || "";
  if (!cardCode) return [];
  const parsed = parseChecklistRemainder(cardCode, clean(line.replace(codeMatches[0]?.raw || cardCode, " ")));
  return parsed ? [parsed] : [];
}

function parseChecklistRemainder(cardCode, remainder) {
  let rest = clean(remainder);
  rest = rest.replace(/\b(auto|autograph|refractor|variation|image variation)\b/gi, " ");
  rest = clean(rest.replace(/^[-–—:|]+/, ""));
  let split = rest.split(/\s[-–—|]\s|\t| {2,}/).map(clean).filter(Boolean);
  if (split.length < 2 && rest.includes(",")) {
    split = rest.split(",").map(clean).filter(Boolean);
  }
  const playerName = cleanName(split[0] || rest);
  const team = clean(split[1] || "");
  if (!playerName || !looksLikePlayerName(playerName)) return null;
  return { cardCode, playerName, team };
}

function cardFromParsed(parsed, subsetName, { year, product, source }) {
  const playerName = cleanName(parsed.playerName);
  return {
    productYear: year,
    productFamily: product.family,
    productName: `${year} ${product.productName}`,
    subsetName,
    cardCode: normalizeCardCode(parsed.cardCode),
    playerName,
    normalizedPlayerName: normalizeName(playerName),
    team: cleanTeam(parsed.team),
    isAuto: /auto|autograph/i.test(subsetName) ? 1 : 1,
    isChrome: /chrome|bowman/i.test(subsetName) ? 1 : 0,
    isFirstBowman: /prospect/i.test(subsetName) ? 1 : 0,
    sourceName: source.sourceName,
    sourceUrl: source.url,
  };
}

function matchCardToPlayer(card, index) {
  const exact = index.byName.get(card.normalizedPlayerName) || [];
  if (exact.length === 1) {
    const player = exact[0];
    return {
      player,
      confidence: teamMatches(card.team, player) ? "high" : "medium",
      sameNameCollision: false,
      notes: teamMatches(card.team, player) ? "Checklist name and team/org matched." : "Checklist name matched; team/org not confirmed.",
    };
  }
  if (exact.length > 1) {
    const teamMatched = exact.filter((player) => teamMatches(card.team, player));
    const player = teamMatched[0] || exact[0];
    return {
      player,
      confidence: teamMatched.length === 1 ? "medium" : "low",
      sameNameCollision: true,
      notes: teamMatched.length === 1
        ? "Same-name collision; team/org narrowed the match but needs review."
        : "Same-name collision; checklist target needs manual review.",
    };
  }
  const fuzzy = fuzzyCandidates(card.normalizedPlayerName, index.players);
  if (fuzzy.length) {
    return {
      player: fuzzy[0],
      confidence: "low",
      sameNameCollision: fuzzy.length > 1,
      notes: "Fuzzy checklist name match; needs manual review.",
    };
  }
  return null;
}

function buildPlayerIndex(rows) {
  const byName = new Map();
  for (const row of rows) {
    const names = new Set([row.normalized_name, normalizeName(row.player_name), normalizeName(row.full_name)].filter(Boolean));
    for (const name of names) {
      if (!byName.has(name)) byName.set(name, []);
      byName.get(name).push(row);
    }
  }
  return { byName, players: rows };
}

function readPlayers() {
  return queryD1(`
    SELECT
      p.id,
      p.player_name,
      p.full_name,
      p.normalized_name,
      p.current_team,
      p.current_org,
      p.position,
      p.active_status,
      pts.tracking_group,
      pts.priority_tier,
      pts.status AS tracking_status
    FROM players p
    LEFT JOIN player_tracking_status pts ON pts.player_id = p.id
  `);
}

