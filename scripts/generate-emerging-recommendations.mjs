#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const args = parseArgs(process.argv.slice(2));
const snapshotDate = args.snapshotDate || new Date().toISOString().slice(0, 10);
const outputPath = path.resolve(args.out || path.join(repoRoot, "data", "imports", "emerging-recommendations.sql"));
const rows = readRecommendationInputs();
const statements = [
  "-- Generated by scripts/generate-emerging-recommendations.mjs.",
  `-- Snapshot date: ${snapshotDate}`,
];
const summary = {
  playersChecked: rows.length,
  recommendationsCreated: 0,
  dryRun: args.dryRun,
};

for (const row of rows) {
  const recommendation = gradeRow(row);
  statements.push(insertRecommendationStatement(row, recommendation));
  summary.recommendationsCreated += 1;
}

statements.push(pipelineRunStatement(summary));

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, statements.join("\n") + "\n");

console.log(JSON.stringify({ ...summary, outputPath }, null, 2));

if (!args.dryRun && rows.length) {
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--file", outputPath];
  if (args.remote) wranglerArgs.push("--remote");
  execFileSync("npx", wranglerArgs, { cwd: repoRoot, stdio: "inherit" });
}

function readRecommendationInputs() {
  const sql = `
    WITH latest_pre AS (
      SELECT pre.*
      FROM emerging_prescore_snapshots pre
      JOIN (
        SELECT player_id, card_target_id, MAX(snapshot_date) AS snapshot_date
        FROM emerging_prescore_snapshots
        GROUP BY player_id, card_target_id
      ) latest ON latest.player_id = pre.player_id
        AND latest.card_target_id = pre.card_target_id
        AND latest.snapshot_date = pre.snapshot_date
    ),
    latest_market AS (
      SELECT m.*
      FROM card_market_snapshots m
      JOIN (
        SELECT player_id, card_target_id, MAX(snapshot_date) AS snapshot_date
        FROM card_market_snapshots
        GROUP BY player_id, card_target_id
      ) latest ON latest.player_id = m.player_id
        AND latest.card_target_id = m.card_target_id
        AND latest.snapshot_date = m.snapshot_date
    )
    SELECT
      p.id AS player_id,
      p.player_name,
      ct.id AS card_target_id,
      ct.year,
      ct.product,
      ct.auto_code,
      ct.card_query_seed,
      pre.emerging_pre_score,
      pre.performance_score,
      pre.age_level_score,
      pre.playing_time_score,
      pre.level_score,
      pre.trend_proxy_score,
      pre.pre_tier,
      pre.pre_score_notes,
      m.sales_count_30d,
      m.sales_count_90d,
      m.avg_price_30d,
      m.avg_price_90d,
      m.last_sold_price,
      m.sell_through_30d,
      m.sell_through_90d,
      m.market_signal
    FROM player_tracking_status pts
    JOIN players p ON p.id = pts.player_id
    JOIN emerging_card_targets ct ON ct.player_id = p.id AND ct.active = 1
    JOIN latest_pre pre ON pre.player_id = p.id AND pre.card_target_id = ct.id
    LEFT JOIN latest_market m ON m.player_id = p.id AND m.card_target_id = ct.id
    WHERE pts.tracking_group = 'emerging'
      AND pts.status = 'active'
      AND pts.priority_tier IN ('card_api_candidate', 'emerging_a', 'emerging_bplus', 'emerging_b')
    ORDER BY pre.emerging_pre_score DESC
    LIMIT ${integer(args.limit)}
  `;
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--command", sql, "--json"];
  if (args.remote) wranglerArgs.push("--remote");
  const stdout = execFileSync("npx", wranglerArgs, { cwd: repoRoot, encoding: "utf8" });
  const parsed = JSON.parse(stdout);
  if (Array.isArray(parsed)) return parsed.flatMap((entry) => entry.results || entry.result?.[0]?.results || []);
  return parsed.results || parsed.result?.[0]?.results || [];
}

function gradeRow(row) {
  const statsScore = clamp(Number(row.emerging_pre_score) || 0, 0, 100);
  const sales90 = Number(row.sales_count_90d);
  const avg30 = Number(row.avg_price_30d);
  const avg90 = Number(row.avg_price_90d);
  const last = Number(row.last_sold_price);
  const liquidityScore = liquidityPoints(sales90);
  const valuationScore = valuationPoints({ avg30, avg90, last });
  const marketScore = marketMomentumPoints(row);
  const total = statsScore * 0.5 + liquidityScore * 0.2 + valuationScore * 0.2 + marketScore * 0.1;
  const noLiquidity = !Number.isFinite(sales90) || sales90 === 0;
  const grade = recommendationGrade(total, noLiquidity, sales90);
  const recommendation = recommendationLabel(grade);
  return {
    statsScore,
    liquidityScore,
    valuationScore,
    marketScore,
    totalScore: Math.round(total * 10) / 10,
    grade,
    recommendation,
    thesis: thesis(row, grade),
    catalyst: catalyst(row),
    riskNotes: riskNotes(row, grade),
  };
}

function insertRecommendationStatement(row, result) {
  return `INSERT INTO recommendation_snapshots (
  player_id, card_target_id, board_type, grade, recommendation,
  total_score, stats_score, market_score, liquidity_score, valuation_score,
  news_score, catalyst_score, thesis, catalyst, risk_notes, status, snapshot_date
) VALUES (
  ${integer(row.player_id)}, ${integer(row.card_target_id)}, 'emerging', ${sql(result.grade)}, ${sql(result.recommendation)},
  ${number(result.totalScore)}, ${number(result.statsScore)}, ${number(result.marketScore)}, ${number(result.liquidityScore)}, ${number(result.valuationScore)},
  NULL, NULL, ${sql(result.thesis)}, ${sql(result.catalyst)}, ${sql(result.riskNotes)}, 'draft', ${sql(snapshotDate)}
)
ON CONFLICT(player_id, card_target_id, board_type, status, snapshot_date) DO UPDATE SET
  grade = excluded.grade,
  recommendation = excluded.recommendation,
  total_score = excluded.total_score,
  stats_score = excluded.stats_score,
  market_score = excluded.market_score,
  liquidity_score = excluded.liquidity_score,
  valuation_score = excluded.valuation_score,
  thesis = excluded.thesis,
  catalyst = excluded.catalyst,
  risk_notes = excluded.risk_notes;`;
}

function pipelineRunStatement(summary) {
  return `INSERT INTO pipeline_runs (
  run_type, started_at, finished_at, status, players_checked, recommendations_created, errors_json
) VALUES (
  'generate_emerging_recommendations', ${sql(new Date().toISOString())}, ${sql(new Date().toISOString())},
  'success', ${integer(summary.playersChecked)}, ${integer(summary.recommendationsCreated)}, '[]'
);`;
}

function liquidityPoints(sales90) {
  if (!Number.isFinite(sales90) || sales90 <= 0) return 0;
  if (sales90 <= 3) return 25;
  if (sales90 <= 9) return 58;
  return 88;
}

function valuationPoints({ avg30, avg90, last }) {
  if (!Number.isFinite(avg30) || avg30 <= 0) return 45;
  if (Number.isFinite(last) && last > 0) {
    const discount = ((avg30 - last) / avg30) * 100;
    if (discount >= 12) return 88;
    if (discount >= 0) return 72;
    if (discount >= -15) return 52;
    return 30;
  }
  if (Number.isFinite(avg90) && avg90 > 0) {
    const move = ((avg30 - avg90) / avg90) * 100;
    if (move <= 10) return 68;
    if (move <= 30) return 48;
    return 28;
  }
  return 45;
}

function marketMomentumPoints(row) {
  const signal = String(row.market_signal || "").toLowerCase();
  if (signal.includes("heating")) return 82;
  if (signal.includes("liquid")) return 68;
  if (signal.includes("thin")) return 32;
  if (signal.includes("priced")) return 24;
  if (Number(row.sales_count_30d) > 0) return 50;
  return 20;
}

function recommendationGrade(total, noLiquidity, sales90) {
  if (noLiquidity) return "No Liquidity";
  if (total >= 90 && sales90 >= 4) return "A+";
  if (total >= 82 && sales90 >= 4) return "A";
  if (total >= 74) return "B+";
  if (total >= 65) return "B";
  if (total >= 50) return "Watch";
  return "Avoid";
}

function recommendationLabel(grade) {
  if (grade === "A+") return "Strong Buy";
  if (grade === "A" || grade === "B+") return "Buy";
  if (grade === "B" || grade === "Watch") return "Watch";
  if (grade === "No Liquidity") return "No Liquidity";
  return "Avoid";
}

function thesis(row, grade) {
  return `${row.player_name} grades ${grade} on the Emerging board from a ${row.emerging_pre_score || "pending"} stats pre-score, ${row.sales_count_90d || 0} sales over 90 days, and ${row.market_signal || "pending"} market signal.`;
}

function catalyst(row) {
  return row.pre_score_notes || "Stats pre-score is the current catalyst until fresh news and market notes are reviewed.";
}

function riskNotes(row, grade) {
  if (grade === "No Liquidity") return "Card has not shown enough 90-day sales to support an aggressive recommendation.";
  if (grade === "Avoid") return "Market setup or baseball signal is not strong enough for a draft buy recommendation.";
  if (!row.sales_count_90d) return "Market data is incomplete; recommendation should stay in draft review.";
  return "Draft recommendation only; news and role path still need manual review.";
}

function parseArgs(argv) {
  const result = { limit: 300, dryRun: false, remote: false, database: "ondeck-market", out: "", snapshotDate: "" };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--limit") result.limit = Math.max(1, Math.min(1000, Number(argv[++index]) || 300));
    else if (arg === "--dry-run") result.dryRun = true;
    else if (arg === "--remote") result.remote = true;
    else if (arg === "--database") result.database = argv[++index] || result.database;
    else if (arg === "--out") result.out = argv[++index] || "";
    else if (arg === "--snapshot-date") result.snapshotDate = argv[++index] || "";
  }
  return result;
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function sql(value) {
  const text = String(value ?? "").replaceAll(/\s+/g, " ").trim();
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function integer(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(Math.round(numeric)) : "NULL";
}

function number(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(numeric) : "NULL";
}
