#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const STATS_API = "https://statsapi.mlb.com/api/v1";
const SPORT_LEVELS = new Map([
  [11, "AAA"],
  [12, "AA"],
  [13, "A+"],
  [14, "A"],
]);
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const args = parseArgs(process.argv.slice(2));
const startedAt = new Date().toISOString();
const snapshotDate = args.snapshotDate || startedAt.slice(0, 10);
const outputPath = path.resolve(args.out || path.join(repoRoot, "data", "imports", "milb-player-universe-refresh.sql"));
const statements = [
  "-- Generated by scripts/refresh-milb-player-universe.mjs.",
  `-- Snapshot date: ${snapshotDate}`,
];
const summary = {
  sportIds: args.sportIds,
  teamsChecked: 0,
  playersSeen: 0,
  playersInsertedOrUpdated: 0,
  errors: [],
};

const mlbOrgNames = await mlbOrgNameMap();

for (const sportId of args.sportIds) {
  const level = SPORT_LEVELS.get(sportId) || String(sportId);
  const teams = await getJson(`${STATS_API}/teams?sportId=${sportId}`).then((data) => data.teams || []);
  for (const team of teams.slice(0, args.teamLimit || teams.length)) {
    summary.teamsChecked += 1;
    try {
      const roster = await getJson(`${STATS_API}/teams/${team.id}/roster?rosterType=active`).then((data) => data.roster || []);
      for (const row of roster.slice(0, args.playerLimit || roster.length)) {
        const personId = Number(row.person?.id);
        if (!Number.isFinite(personId)) continue;
        summary.playersSeen += 1;
        const person = await getJson(`${STATS_API}/people/${personId}?hydrate=currentTeam`).then((data) => data.people?.[0] || {});
        const player = normalizePerson(person, row, team, level);
        statements.push(upsertPlayerStatement(player));
        statements.push(playerSourceStatement(player, "MiLB Roster Refresh", `${team.name} active roster`, `${level} roster refresh from MLB StatsAPI.`));
        summary.playersInsertedOrUpdated += 1;
      }
    } catch (error) {
      summary.errors.push({ team: team.name, sportId, message: error instanceof Error ? error.message : String(error) });
    }
  }
}

statements.push(refreshRunStatement("refresh_milb_player_universe", summary));
writeAndMaybeExecute();

function normalizePerson(person, rosterRow, team, level) {
  const currentTeam = person.currentTeam || team;
  const parentOrgId = currentTeam.parentOrgId || team.parentOrgId;
  const fullName = clean(person.fullFMLName || person.fullName || rosterRow.person?.fullName);
  const playerName = clean(person.fullName || rosterRow.person?.fullName || fullName);
  return {
    mlbamId: Number(person.id || rosterRow.person?.id),
    playerName,
    fullName,
    normalizedName: normalizeName(fullName || playerName),
    middleName: clean(person.middleName),
    currentTeam: clean(currentTeam.name || team.name),
    currentOrg: clean(mlbOrgNames.get(Number(parentOrgId)) || currentTeam.parentOrgName || team.parentOrgName),
    currentLevel: level,
    position: clean(person.primaryPosition?.abbreviation || rosterRow.position?.abbreviation),
    bats: clean(person.batSide?.code),
    throws: clean(person.pitchHand?.code),
    birthDate: clean(person.birthDate),
    age: number(person.currentAge),
    draftYear: integer(person.draftYear),
    height: clean(person.height),
    weight: integer(person.weight),
    raw: JSON.stringify({ personId: person.id, currentTeamId: currentTeam.id, source: "MLB StatsAPI" }),
  };
}

async function mlbOrgNameMap() {
  const teams = await getJson(`${STATS_API}/teams?sportId=1`).then((data) => data.teams || []).catch(() => []);
  return new Map(teams.map((team) => [Number(team.id), clean(team.name)]));
}

async function getJson(url) {
  const response = await fetch(url, { headers: { Accept: "application/json" } });
  if (!response.ok) throw new Error(`${url} returned ${response.status}`);
  return response.json();
}

function upsertPlayerStatement(player) {
  return `UPDATE players SET
  mlbam_id = ${integer(player.mlbamId)},
  identity_key = ${sql(`mlbam:${player.mlbamId}`)},
  player_name = ${sql(player.playerName)},
  full_name = ${sql(player.fullName)},
  middle_name = ${sql(player.middleName)},
  current_team = ${sql(player.currentTeam)},
  current_org = ${sql(player.currentOrg)},
  current_level = ${sql(player.currentLevel)},
  position = ${sql(player.position)},
  bats = ${sql(player.bats)},
  throws = ${sql(player.throws)},
  birth_date = ${sql(player.birthDate)},
  age = ${number(player.age)},
  active_status = 'active',
  first_seen_source = COALESCE(first_seen_source, 'MiLB Roster Refresh'),
  last_seen_date = ${sql(snapshotDate)},
  draft_year = ${integer(player.draftYear)},
  height = ${sql(player.height)},
  weight = ${integer(player.weight)},
  raw_identity_json = ${sql(player.raw)},
  updated_at = CURRENT_TIMESTAMP
WHERE mlbam_id = ${integer(player.mlbamId)} OR (mlbam_id IS NULL AND normalized_name = ${sql(player.normalizedName)});

INSERT INTO players (
  player_name, normalized_name, mlbam_id, identity_key, full_name, middle_name,
  current_team, current_org, current_level, position, bats, throws, birth_date, age,
  active_status, first_seen_source, last_seen_date, draft_year, height, weight, raw_identity_json
)
SELECT
  ${sql(player.playerName)}, ${sql(player.normalizedName)}, ${integer(player.mlbamId)}, ${sql(`mlbam:${player.mlbamId}`)},
  ${sql(player.fullName)}, ${sql(player.middleName)}, ${sql(player.currentTeam)}, ${sql(player.currentOrg)},
  ${sql(player.currentLevel)}, ${sql(player.position)}, ${sql(player.bats)}, ${sql(player.throws)},
  ${sql(player.birthDate)}, ${number(player.age)}, 'active', 'MiLB Roster Refresh', ${sql(snapshotDate)},
  ${integer(player.draftYear)}, ${sql(player.height)}, ${integer(player.weight)}, ${sql(player.raw)}
WHERE NOT EXISTS (SELECT 1 FROM players WHERE mlbam_id = ${integer(player.mlbamId)})
  AND NOT EXISTS (SELECT 1 FROM players WHERE normalized_name = ${sql(player.normalizedName)});`;
}

function playerSourceStatement(player, sourceType, sourceName, notes) {
  return `INSERT OR IGNORE INTO player_sources (player_id, source_type, source_name, source_date, source_notes)
SELECT id, ${sql(sourceType)}, ${sql(sourceName)}, ${sql(snapshotDate)}, ${sql(notes)}
FROM players
WHERE mlbam_id = ${integer(player.mlbamId)};`;
}

function refreshRunStatement(runType, result) {
  return `INSERT INTO player_refresh_runs (
  run_type, started_at, finished_at, status, sport_ids, teams_checked,
  players_seen, players_inserted, players_updated, errors_json
) VALUES (
  ${sql(runType)}, ${sql(startedAt)}, ${sql(new Date().toISOString())}, ${sql(result.errors.length ? "partial" : "success")},
  ${sql(result.sportIds.join(","))}, ${integer(result.teamsChecked)}, ${integer(result.playersSeen)},
  ${integer(result.playersInsertedOrUpdated)}, 0, ${sql(JSON.stringify(result.errors))}
);`;
}

function writeAndMaybeExecute() {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, statements.join("\n") + "\n");
  console.log(JSON.stringify({ ...summary, outputPath }, null, 2));
  if (!args.execute) return;
  const wranglerArgs = ["wrangler", "d1", "execute", args.database, "--file", outputPath];
  if (args.remote) wranglerArgs.push("--remote");
  execFileSync("npx", wranglerArgs, { cwd: repoRoot, stdio: "inherit" });
}

function parseArgs(argv) {
  const result = {
    sportIds: [11, 12, 13, 14],
    execute: false,
    remote: false,
    database: "ondeck-market",
    out: "",
    snapshotDate: "",
    teamLimit: 0,
    playerLimit: 0,
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--sport-ids") result.sportIds = String(argv[++index] || "").split(",").map((id) => Number(id.trim())).filter(Number.isFinite);
    else if (arg === "--execute") result.execute = true;
    else if (arg === "--remote") result.remote = true;
    else if (arg === "--database") result.database = argv[++index] || result.database;
    else if (arg === "--out") result.out = argv[++index] || "";
    else if (arg === "--snapshot-date") result.snapshotDate = argv[++index] || "";
    else if (arg === "--team-limit") result.teamLimit = Math.max(0, Number(argv[++index]) || 0);
    else if (arg === "--player-limit") result.playerLimit = Math.max(0, Number(argv[++index]) || 0);
  }
  return result;
}

function normalizeName(value) {
  return clean(value).normalize("NFD").replaceAll(/[\u0300-\u036f]/g, "").toLowerCase().replaceAll(/[^a-z0-9]+/g, " ").trim();
}

function clean(value) {
  return String(value ?? "").replaceAll(/\s+/g, " ").trim();
}

function sql(value) {
  const text = clean(value);
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function integer(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(Math.round(numeric)) : "NULL";
}

function number(value) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(numeric) : "NULL";
}
