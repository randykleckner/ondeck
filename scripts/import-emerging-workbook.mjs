function cardFields(row, includeInEmerging, status) {
  return {
    year: row.year,
    product: clean(row.product),
    autoSet: clean(row.auto_set),
    autoCode: clean(row.auto_code_card || row.auto_code || row.card_number),
    playerName: clean(row.player),
    teamOnCard: clean(row.team_on_card),
    cardQuerySeed: clean(row.card_query_seed),
    sourceUrl: clean(row.source_url),
    includeInEmerging,
    active: status === "removed" ? 0 : 1,
    notes: clean(row.notes),
  };
}

function cardTargetIdSql(playerRef, card) {
  return `(SELECT id FROM emerging_card_targets WHERE player_id = ${playerRef} AND year = ${integer(card.year)} AND product IS ${nullSafeIs(card.product)} AND auto_code IS ${nullSafeIs(card.autoCode)} LIMIT 1)`;
}

function playerIdSql(normalizedName) {
  return `(SELECT id FROM players WHERE normalized_name = ${sql(normalizedName)} LIMIT 1)`;
}

function trackingNotes(row, sheetName) {
  return [sheetName, row.filter_reason, row.removal_reason, row.notes].map(clean).filter(Boolean).join(" | ");
}

function hasStats(row) {
  return Boolean(clean(row.stats_role) || clean(row.h_pa) || clean(row.p_ip));
}

function hasPrescore(row) {
  return clean(row.emerging_pre_score) !== "";
}

function normalizeTier(value) {
  const normalized = clean(value).toLowerCase().replace(/\+/g, "plus").replaceAll(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");
  if (normalized === "card_api_candidate") return "card_api_candidate";
  if (normalized === "emerging_a") return "emerging_a";
  if (normalized === "emerging_bplus" || normalized === "emerging_b_plus") return "emerging_bplus";
  if (normalized === "emerging_b") return "emerging_b";
  if (normalized === "emerging_watch") return "emerging_watch";
  if (normalized === "low_priority") return "low_priority";
  return normalized;
}

function tierFromSheet(sheetName) {
  return normalizeTier(sheetName.replace("BPlus", "B+"));
}

function watchStatus(tier) {
  return tier === "emerging_watch" || tier === "low_priority" ? "watch" : "active";
}

function nextRefreshDate() {
  const date = new Date(startedAt);
  date.setUTCDate(date.getUTCDate() + 14);
  return date.toISOString();
}

function parseArgs(argv) {
  const result = { input: "", out: "", execute: false, remote: false, database: "ondeck-market", snapshotDate: "" };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--execute") result.execute = true;
    else if (arg === "--remote") {
      result.remote = true;
      result.execute = true;
    } else if (arg === "--out") result.out = argv[++index] || "";
    else if (arg === "--database") result.database = argv[++index] || result.database;
    else if (arg === "--snapshot-date") result.snapshotDate = argv[++index] || "";
    else if (!arg.startsWith("--") && !result.input) result.input = arg;
  }
  return result;
}

function parseCsv(text) {
  const rows = [];
  let field = "";
  let row = [];
  let quoted = false;
  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];
    if (char === "\"" && quoted && next === "\"") {
      field += "\"";
      index += 1;
    } else if (char === "\"") {
      quoted = !quoted;
    } else if (char === "," && !quoted) {
      row.push(field);
      field = "";
    } else if ((char === "\n" || char === "\r") && !quoted) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(field);
      if (row.some((value) => clean(value) !== "")) rows.push(row);
      row = [];
      field = "";
    } else {
      field += char;
    }
  }
  row.push(field);
  if (row.some((value) => clean(value) !== "")) rows.push(row);
  const [headers = [], ...records] = rows;
  return records.map((record) => Object.fromEntries(headers.map((header, index) => [normalizeHeader(header), clean(record[index])])));
}

function normalizeHeader(value) {
  return clean(value).toLowerCase().replaceAll(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");
}

function normalizeName(value) {
  return clean(value)
    .normalize("NFD")
    .replaceAll(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replaceAll(/[^a-z0-9]+/g, " ")
    .trim();
}

function clean(value) {
  return String(value ?? "").replaceAll(/\s+/g, " ").trim();
}

function sql(value) {
  const text = clean(value);
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function nullSafeIs(value) {
  const text = clean(value);
  return text ? `'${text.replaceAll("'", "''")}'` : "NULL";
}

function number(value) {
  const text = clean(value).replaceAll("%", "");
  if (!text) return "NULL";
  const numeric = Number(text);
  return Number.isFinite(numeric) ? String(numeric) : "NULL";
}

function integer(value) {
  const text = clean(value).replaceAll(/[^0-9.-]/g, "");
  if (!text) return "NULL";
  const numeric = Number(text);
  return Number.isFinite(numeric) ? String(Math.round(numeric)) : "NULL";
}
