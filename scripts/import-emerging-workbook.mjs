#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const defaultSource = path.join(repoRoot, "Emerging_cleaned");
const defaultOut = path.join(repoRoot, "data", "imports", "emerging-import.sql");
const args = parseArgs(process.argv.slice(2));
const sourcePath = path.resolve(args.input || defaultSource);
const outputPath = path.resolve(args.out || defaultOut);
const snapshotDate = args.snapshotDate || new Date().toISOString().slice(0, 10);
const sourceWorkbook = path.basename(sourcePath);
const startedAt = new Date().toISOString();

if (sourcePath.toLowerCase().endsWith(".xlsx")) {
  console.error("XLSX input is not available in this dependency-free repo. Export the Numbers workbook as CSV sheets into Emerging_cleaned/ and rerun this script.");
  process.exit(1);
}

const sheets = loadCsvSource(sourcePath);
const counts = {};
const statements = [
  "-- Generated by scripts/import-emerging-workbook.mjs.",
  `-- Source: ${sourcePath}`,
  `-- Snapshot date: ${snapshotDate}`,
];

const seenStatsKeys = new Set();
const seenPrescoreKeys = new Set();

importSheet("All_Targets", {
  defaultGroup: "emerging",
  statusForRow: (row) => watchStatus(normalizeTier(row.pre_tier)),
  tierForRow: (row) => normalizeTier(row.pre_tier) || "emerging_watch",
  importStats: true,
  importPrescore: true,
});

for (const sheetName of ["Emerging_A", "Emerging_BPlus", "Emerging_B", "Emerging_Watch", "Low_Priority"]) {
  importSheet(sheetName, {
    defaultGroup: "emerging",
    defaultStatus: sheetName === "Emerging_Watch" || sheetName === "Low_Priority" ? "watch" : "active",
    defaultTier: tierFromSheet(sheetName),
    importStats: true,
    importPrescore: true,
  });
}

importSheet("Card_API_Candidates", {
  defaultGroup: "emerging",
  defaultStatus: "active",
  defaultTier: "card_api_candidate",
  importStats: true,
  importPrescore: true,
});

importSheet("Called_Up", {
  defaultGroup: "called_up",
  defaultStatus: "removed",
  defaultTier: "called_up",
  importStats: false,
  importPrescore: false,
  includeInEmerging: 0,
});

for (const sheetName of ["Top100_Removed", "Top100_Removed_Previous"]) {
  importSheet(sheetName, {
    defaultGroup: "top100",
    defaultStatus: "removed",
    defaultTier: "top100_removed",
    importStats: sheetName === "Top100_Removed",
    importPrescore: sheetName === "Top100_Removed",
    includeInEmerging: 0,
  });
}

importSheet("Inactive_No_2026_Stats", {
  defaultGroup: "inactive",
  defaultStatus: "removed",
  defaultTier: "inactive_no_2026_stats",
  importStats: false,
  importPrescore: false,
  includeInEmerging: 0,
});

const finishedAt = new Date().toISOString();
statements.push(pipelineRunStatement({
  startedAt,
  finishedAt,
  playersChecked: Object.values(counts).reduce((total, value) => total + value.rows, 0),
  statsUpdated: Object.values(counts).reduce((total, value) => total + value.stats, 0),
  top100Removed: (counts.Top100_Removed?.rows || 0) + (counts.Top100_Removed_Previous?.rows || 0),
  calledUpRemoved: counts.Called_Up?.rows || 0,
  inactiveRemoved: counts.Inactive_No_2026_Stats?.rows || 0,
  cardTargetsChecked: Object.values(counts).reduce((total, value) => total + value.cards, 0),
  errorsJson: "[]",
}));

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, statements.join("\n") + "\n");

console.log(JSON.stringify({
  sourcePath,
  outputPath,
  snapshotDate,
  sheets: counts,
}, null, 2));

if (args.execute) {
  const wranglerArgs = ["wrangler", "d1", "execute", args.database || "ondeck-market", "--file", outputPath];
  if (args.remote) wranglerArgs.push("--remote");
  console.log(`Executing: npx ${wranglerArgs.join(" ")}`);
  execFileSync("npx", wranglerArgs, { cwd: repoRoot, stdio: "inherit" });
}

function importSheet(sheetName, options) {
  const rows = sheets.get(sheetName) || [];
  counts[sheetName] = { rows: rows.length, players: 0, cards: 0, stats: 0, prescores: 0 };
  for (const row of rows) {
    const playerName = clean(row.player || row.workbook_player);
    const normalizedName = normalizeName(playerName);
    if (!playerName || !normalizedName) continue;

    const tier = options.tierForRow ? options.tierForRow(row) : options.defaultTier;
    const status = options.statusForRow ? options.statusForRow(row) : options.defaultStatus;
    const trackingGroup = options.defaultGroup;
    const includeInEmerging = options.includeInEmerging ?? (trackingGroup === "emerging" ? 1 : 0);
    const playerRef = playerIdSql(normalizedName);
    const card = cardFields(row, includeInEmerging, status);
    const hasCard = Boolean(card.autoCode || card.cardQuerySeed);

    statements.push(upsertPlayerStatement(row, playerName, normalizedName));
    counts[sheetName].players += 1;

    if (hasCard) {
      statements.push(upsertCardTargetStatement(playerRef, card));
      counts[sheetName].cards += 1;
    }

    statements.push(upsertTrackingStatement({
      playerRef,
      trackingGroup,
      priorityTier: tier,
      status,
      notes: trackingNotes(row, sheetName),
    }));

    if (options.importStats && hasStats(row)) {
      const key = `${normalizedName}:${snapshotDate}:${clean(row.stats_role)}:${clean(row.stats_level)}:${clean(row.stats_team)}`;
      if (!seenStatsKeys.has(key)) {
        seenStatsKeys.add(key);
        statements.push(upsertStatsStatement(playerRef, row));
        counts[sheetName].stats += 1;
      }
    }

    if (options.importPrescore && hasPrescore(row)) {
      const key = `${normalizedName}:${card.autoCode}:${snapshotDate}`;
      if (!seenPrescoreKeys.has(key)) {
        seenPrescoreKeys.add(key);
        statements.push(upsertPrescoreStatement(playerRef, hasCard ? cardTargetIdSql(playerRef, card) : "NULL", row, tier));
        counts[sheetName].prescores += 1;
      }
    }
  }
}

function loadCsvSource(source) {
  if (!fs.existsSync(source)) {
    throw new Error(`Input path not found: ${source}`);
  }
  if (!fs.statSync(source).isDirectory()) {
    throw new Error(`Expected a folder of exported CSV sheets, received: ${source}`);
  }
  const map = new Map();
  for (const file of fs.readdirSync(source).filter((name) => name.endsWith(".csv")).sort()) {
    const sheetName = file.replace(/-Table 1\.csv$/i, "").replace(/\.csv$/i, "");
    map.set(sheetName, parseCsv(fs.readFileSync(path.join(source, file), "utf8")));
  }
  return map;
}

function upsertPlayerStatement(row, playerName, normalizedName) {
  const mlbam = integer(row.mlbam_id || row.stats_player_id);
  const currentTeam = clean(row.stats_team);
  const currentOrg = clean(row.team_on_card);
  return `INSERT INTO players (
  player_name, normalized_name, mlbam_id, current_team, current_org, position, updated_at
) VALUES (
  ${sql(playerName)}, ${sql(normalizedName)}, ${mlbam}, ${sql(currentTeam)}, ${sql(currentOrg)}, ${sql(clean(row.position))}, CURRENT_TIMESTAMP
)
ON CONFLICT(normalized_name) DO UPDATE SET
  player_name = excluded.player_name,
  mlbam_id = COALESCE(excluded.mlbam_id, players.mlbam_id),
  current_team = COALESCE(excluded.current_team, players.current_team),
  current_org = COALESCE(excluded.current_org, players.current_org),
  position = COALESCE(excluded.position, players.position),
  updated_at = CURRENT_TIMESTAMP;`;
}

